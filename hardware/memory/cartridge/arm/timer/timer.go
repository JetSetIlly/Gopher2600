package timer

// Timer is an abstraction of the various types of timer found in the difference
// ARM CPU pacakages
type Timer interface {
	Reset()
	Step(cycles float32)
	Write(addr uint32, val uint32) bool
	Read(addr uint32) (uint32, bool)
}
