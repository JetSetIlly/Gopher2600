func expectEqualPalettes(t *testing.T, a []uint32, b []uint32) {
	test.ExpectEquality(t, len(a), len(b))
	for i := range a {
		test.ExpectEquality(t, a[i], b[i])
	}
}

func expectUnequalPalettes(t *testing.T, a []uint32, b []uint32) {
	test.ExpectEquality(t, len(a), len(b))
	equality := true
	for i := range a {
		if a[i] != b[i] {
			equality = false
			break // for loop
		}
	}
	test.ExpectFailure(t, equality)
}

